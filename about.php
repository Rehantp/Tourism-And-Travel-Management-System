			<h1 align="center">About Tourism In Sri Lanka</h1>
			<!-- Intro para 1 -->
			<p align="justify">The government initiatives in development of tourism date back to 1937 when the Ceylon Tourist Bureau was established. However, it was closed down in September 1939 due to World War II. After Sri Lanka's independence the promotion of tourism was again considered by re-establishing the Ceylon Tourist Board which took over the function of the Tourist Bureau.<br><br>
			In 1965, J. R. Jayewardene as Minister of State initiated the first major development of the tourism industry with the Ceylon Tourist Board Act No 10 of 1966 and the Ceylon Hotels Corporation Act No 14 of 1966, which formed the Ceylon Tourist Board and the Ceylon Hotels Corporation. Since then the Ceylon Tourist Board has functioned as the state agency, responsible for development and promotion of the tourism sector in Sri Lanka. Ceylon Hotels Corporation paved the way government investments build hotels needed to attract tourist.<br><br>
			In October 2007 according to Section 2 of the Tourism Act No. 38 of 2005, the Sri Lanka Tourist Board (Act No 10 of 1966) was replaced by the Sri Lanka Tourism Development Authority (SLTDA).<br><br>
			Currently Sri Lanka Tourism Development Authority has classified Sri Lanka into several resort regions suitable for tourism development</p>
